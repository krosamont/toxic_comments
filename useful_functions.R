preclean = function(x){
        df = x
        #Enlever les dieses
        #deja fait dans le .rds
        preclean = gsub("c\\(\""," ", df)
        preclean = gsub("\\\\n"," ", preclean )
        preclean = gsub("\\n"," ", preclean )
        preclean = gsub("“|„"," ", preclean )
        preclean = removePunctuation(preclean, preserve_intra_word_contractions = TRUE)
        preclean = stripWhitespace(preclean)
        return(preclean)
}

freqWord = function(x){
        as.data.frame(table(x)) %>%
                arrange(desc(Freq)) %>%
                mutate(type=length(x), total=sum(Freq)) %>%
                filter(Freq>10 & length(x)>0) %>%
                rename(word=x)
}

