length_comments = function(x){
        comment = strsplit(x, " ")
        l_comment = comment %>%
                map(length) %>%
                reduce(rbind)
        l_unique = comment %>%
                map(unique) %>%
                map(length) %>%
                reduce(rbind)
        
        res = data.frame(l_comment = l_comment, l_unique = l_unique, unique_per_l = l_unique/l_comment, row.names = NULL )
        
        return(res)
}      


